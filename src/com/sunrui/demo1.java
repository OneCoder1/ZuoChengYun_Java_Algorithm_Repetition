package com.sunrui;

public class demo1 {
    public static void main(String[] args) {
        //八大基本数据类型
        //整数
        int num1 = 10;
        byte num2 = 20;
        long num3 = 30L;
        short num4 = 40;

        //小数：浮点数
        //需要加个F来表示踏实浮点型
        float num5 = 50.1F;
        double num6 = 3.141592642425152356;

        //字符
        char letter = 'A';
        //字符串String 不是关键字，而是一个类
        String name = "JudeJojoker";

        //布尔值
        boolean flag = true;
    }
}
